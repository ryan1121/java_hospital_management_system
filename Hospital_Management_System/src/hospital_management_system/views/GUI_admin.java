    private void updateRole() {
        int selectedIndex = jTabbedPane1.getSelectedIndex();
        if (selectedIndex == 0) {
            role = "Doctor";
        } else if (selectedIndex == 1) {
            role = "Nurse";
        }
        System.out.println("Updated Role: " + role); // Debug statement
    }

